public class dataTypes{
	public static void main(String[] args){	
		byte x = 2;
		int age = 10;
		float rateOfInterest = 2.3f;
		double rate = 3.14535581;
		boolean isTrue = true;
		char myChar = 'A';
		System.out.println("This is byte type:");
		System.out.println(x);
		System.out.println("This is integer type:");
		System.out.println(age);
		System.out.println("This is float type:");
		System.out.println(rateOfInterest);
		System.out.println("This is double type:");
		System.out.println(rate);
		System.out.println("This is boolean type:");
		System.out.println(isTrue);
		System.out.println("This is character type:");
		System.out.println(myChar);

  	}
}